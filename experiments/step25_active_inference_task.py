def _eval_agent(
    model, tasks: list[tuple], *, beta: float, oracle: bool, base_seed: int, **kw
) -> list[dict]:
    r"""Run every POMDP once with a per-task CEM seed (paired design across agents)."""
    out = []
    for i, task in enumerate(tasks):
        gen = torch.Generator().manual_seed(base_seed + i)
        out.append(closed_loop_pomdp(model, task, beta=beta, oracle=oracle, gen=gen, **kw))
    return out


