# --------------------------------------------------------------------------- #
# belief bookkeeping
# --------------------------------------------------------------------------- #
def belief_entropy(p: float) -> float:
    r"""Binary belief entropy $\mathcal H(p)=-p\ln p-(1-p)\ln(1-p)$ (nats); $0$ when $p\in\{0,1\}$."""
    if p <= 0.0 or p >= 1.0:
        return 0.0
    return -p * math.log(p) - (1.0 - p) * math.log(1.0 - p)


def _zscore(v: torch.Tensor) -> torch.Tensor:
    r"""Standardise across the candidate population (constant -> all-zero, stably)."""
    return (v - v.mean()) / v.std().clamp_min(1e-8)


# --------------------------------------------------------------------------- #
# the ambiguous-goal cue-foraging POMDP
# --------------------------------------------------------------------------- #
def make_cue_tasks(
    k: int, *, seed: int, h_goal: int = 4, h_cue: int = 3, a_goal: float = 0.8, a_cue: float = 0.8,
) -> list[tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor, int]]:
    r"""``k`` POMDPs ``(X0, Xc, Xg_plus, Xg_minus, b)`` -- all clouds teacher-reachable from $X_0$.

    For each task we draw a primary axis $n_g$ and a *transverse* cue axis $n_c\perp n_g$
    (Gram-Schmidt), then roll the exactly-equivariant teacher:

      * $g_+$: ``h_goal`` steps along $+a_{\rm goal}\,n_g$  -> pose+centroid at $+d\,n_g$,
      * $g_-$: ``h_goal`` steps along $-a_{\rm goal}\,n_g$  -> the mirror pose+centroid at $-d\,n_g$,
      * cue  : ``h_cue``  steps along $+a_{\rm cue}\,n_c$   -> a transverse detour (neither goal).

    Because $g_\pm$ are mirror images about $X_0$, their centroid midpoint is the start centroid and
    their latent midpoint is $\approx z_{X_0}$: a belief-myopic agent that hedges to the midpoint
    therefore sits a full $d$ from *either* true goal (the irreducible hedge floor). The hidden index
    $b\in\{+1,-1\}$ is uniform. Returns ``X*: (1,P,3)`` at :data:`EVAL_DTYPE`.
    """
    rng = np.random.default_rng(seed)
    tasks: list[tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor, int]] = []

    def roll(X: torch.Tensor, axis: np.ndarray, mag: float, h: int) -> torch.Tensor:
        for _ in range(h):
            a = mag * axis + 0.10 * rng.standard_normal(3)        # persistent axis + small noise
            a = np.clip(a, -1.0, 1.0).astype(np.float32)
            X = teacher_step(X, torch.from_numpy(a).reshape(1, 3).to(EVAL_DTYPE))
        return X

    for _ in range(k):
        jitter = rng.standard_normal((N_POINTS, 3)).astype(np.float32) * 0.04
        X0 = torch.from_numpy(_TEMPLATE + jitter).unsqueeze(0).to(EVAL_DTYPE)   # (1,P,3)
        n_g = rng.standard_normal(3)
        n_g = n_g / (np.linalg.norm(n_g) + 1e-9)
        r = rng.standard_normal(3)
        n_c = r - (r @ n_g) * n_g                                  # project out n_g  (transverse)
        n_c = n_c / (np.linalg.norm(n_c) + 1e-9)
        Xgp = roll(X0.clone(), n_g, a_goal, h_goal)
        Xgm = roll(X0.clone(), -n_g, a_goal, h_goal)
        Xc = roll(X0.clone(), n_c, a_cue, h_cue)
        b = 1 if rng.random() < 0.5 else -1
        tasks.append((X0, Xc, Xgp, Xgm, b))
    return tasks


# --------------------------------------------------------------------------- #
# the EFE planner for the POMDP: belief-weighted pragmatic - beta * salience
# --------------------------------------------------------------------------- #
@torch.no_grad()
def efe_pomdp_plan(
    model, X0: torch.Tensor, *, p: float, zgp: torch.Tensor, zgm: torch.Tensor,
    cgp: torch.Tensor, cgm: torch.Tensor, zc: torch.Tensor, delta: float, beta: float,
    R_noise: torch.Tensor | None = None, w_t: float = 0.5, H: int = 12, n_samples: int = 256,
    n_iters: int = 5, n_elite: int = 25, sigma0: float = 0.6, w_run: float = 0.3,
    gen: torch.Generator | None = None,
) -> torch.Tensor:
    r"""SE(3)-equivariant CEM minimising the POMDP EFE $G=\text{(belief-weighted pragmatic)}-\beta\,\text{salience}$.

    Identical equivariant scaffolding to Step 18's :func:`latent_cem_plan_iso` (iso-$\sigma$, ball
    clamp, $R$-rotated noise, closed-form centroid channel), with two changes:
      * the pragmatic latent + centroid costs are **belief-weighted** between $g_+$ and $g_-$, and
      * an **epistemic salience** $-\beta\,\eta\,\mathcal H(p)$ is added, where
        $\eta=1-\prod_h\big(1-e^{-\lVert\hat z_h-z_c\rVert^2/2\delta^2}\big)$ is the imagined probability
        the rolled latent senses the cue.
    The three channels -- belief-weighted latent (pose) match, exact-centroid match, salience -- are
    **z-scored separately** across the (jointly rotated) candidate population and combined as
    $\hat{\text{lat}} + w_t\,\hat{\text{cen}} - \beta\,\hat{\text{sal}}$. Separate standardisation is
    essential: the latent term sums over $D=48$ dims and accumulates over $H$, so in raw units it is
    $\sim\!100\times$ the 3-D centroid term and would swamp it (even the oracle never reaches its goal);
    per-channel z-scoring makes $w_t,\beta$ clean dimensionless trade-offs. Each channel's per-candidate
    value is $\mathrm{SE}(3)$-invariant, so its population mean/std are invariant scalars and the whole
    EFE stays exactly $\mathrm{SE}(3)$-invariant -- the equivariance theorem is preserved.

    Shapes: ``X0:(1,P,3); zg*,zc:(1,D); cg*:(3,)|(1,3)``. Returns the plan ``(H,3)`` in the unit ball.
    """
    dtype = model.encoder(X0).dtype
    z0 = model.encoder(X0)                                   # (1,D)
    K = n_samples
    c0 = centroid(X0).reshape(1, 3)
    cgp, cgm = cgp.reshape(1, 3), cgm.reshape(1, 3)
    zgp_e, zgm_e, zc_e = zgp.expand(K, -1), zgm.expand(K, -1), zc.expand(K, -1)
    Hp = belief_entropy(p)                                   # scalar: 0 once b is observed -> drive off
    Rn = None if R_noise is None else R_noise.to(dtype)
    two_d2 = 2.0 * delta * delta
    mean = torch.zeros(H, 3, dtype=dtype)
    sigma = torch.full((H, 3), sigma0, dtype=dtype)

    def rollout(cand: torch.Tensor) -> tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
        r"""Belief-weighted latent-match cost, exact-centroid cost, and salience $\eta$. ``cand:(ns,H,3)``."""
        ns = cand.shape[0]
        z = z0.expand(ns, -1).contiguous()
        prag_lat = torch.zeros(ns, dtype=dtype)              # belief-weighted latent (pose) match
        notsensed = torch.ones(ns, dtype=dtype)              # running prod_h (1 - s_h)
        for h in range(H):
            z = model.predictor(z, cand[:, h])               # (ns,D)
            d2 = p * ((z - zgp_e[:ns]) ** 2).sum(-1) + (1.0 - p) * ((z - zgm_e[:ns]) ** 2).sum(-1)
            prag_lat = prag_lat + (w_run * d2 if h < H - 1 else d2)
            s_h = torch.exp(-((z - zc_e[:ns]) ** 2).sum(-1) / two_d2)     # cue sensor activation
            notsensed = notsensed * (1.0 - s_h)
        # exact, closed-form translation channel: terminal centroid under drift c_t * sum_h a_h
        pred_centroid = c0 + C_T * cand.sum(dim=1)           # (ns,3)
        cen = p * ((pred_centroid - cgp) ** 2).sum(-1) + (1.0 - p) * ((pred_centroid - cgm) ** 2).sum(-1)
        eta = 1.0 - notsensed                                # prob. of sensing the cue at least once
        return prag_lat, cen, eta

    for _ in range(n_iters):
        eps = torch.randn(n_samples, H, 3, generator=gen, dtype=dtype)
        if Rn is not None:
            eps = torch.einsum("ij,nhj->nhi", Rn, eps)       # rotate exploration noise (equivariance)
        cand = _ball_clamp(mean.unsqueeze(0) + sigma.unsqueeze(0) * eps)
        prag_lat, cen, eta = rollout(cand)
        salience = eta * Hp                                  # expected belief-entropy reduction
        # z-score EACH channel separately: the latent-match term sums over D=48 dims AND accumulates
        # over H, so in raw units it is ~100x the 3-D exact-centroid term and would otherwise swamp it
        # (even the oracle never reaches its goal). Standardising per channel puts them on a common
        # dimensionless scale, so w_t (centroid) and beta (salience) are clean trade-offs -- and since
        # each channel's per-candidate values are SE(3)-invariant, their population mean/std are
        # invariant scalars, so the whole EFE stays exactly SE(3)-invariant (the theorem is preserved).
        cost = _zscore(prag_lat) + w_t * _zscore(cen) - beta * _zscore(salience)
        elite = cand[torch.topk(cost, n_elite, largest=False).indices]
        mean = elite.mean(0)
        var_iso = ((elite - mean.unsqueeze(0)) ** 2).mean(dim=(0, 2))     # isotropic per-step std
        sigma = var_iso.sqrt().clamp_min(1e-3).unsqueeze(-1).expand(H, 3)
    return mean


# --------------------------------------------------------------------------- #
# the closed loop: plan -> execute on the teacher -> sense the cue -> Bayesian collapse -> commit
# --------------------------------------------------------------------------- #
@torch.no_grad()
def closed_loop_pomdp(
    model, task: tuple, *, beta: float, R_noise: torch.Tensor | None = None,
    R: torch.Tensor | None = None, t: torch.Tensor | None = None, oracle: bool = False,
    delta_frac: float = 0.6, w_t: float = 0.5, T_max: int = 18, replan_every: int = 6,
    gen: torch.Generator | None = None, **cem,
) -> dict:
    r"""Run one POMDP episode under the EFE planner; optionally on a globally transformed copy $(R,t)$.

    The cue is *sensed* whenever the executed latent comes within $\delta=\texttt{delta\_frac}\cdot
    \lVert z_c-z_0\rVert$ of the cue latent; the first sense collapses the belief to the true index
    (a noiseless one-bit Bayesian update), after which $\mathcal H(p)=0$ turns the salience off and the
    planner commits to the true goal. ``oracle=True`` starts with the belief already collapsed
    (the achievable floor). Returns the **true-goal** error (Kabsch angle + centroid distance), whether
    the cue was sensed, and the min latent distance achieved to the cue.
    """
    X0, Xc, Xgp, Xgm, b = task
    if R is not None:                                        # transform the whole episode by (R,t)
        X0, Xc, Xgp, Xgm = (_apply(Z, R, t) for Z in (X0, Xc, Xgp, Xgm))
    Xtrue = Xgp if b == 1 else Xgm
    zgp, zgm, zc = model.encoder(Xgp), model.encoder(Xgm), model.encoder(Xc)
    cgp, cgm = centroid(Xgp), centroid(Xgm)
    z0 = model.encoder(X0)
    delta = delta_frac * float((zc - z0).norm()) + 1e-9
    p = (1.0 if b == 1 else 0.0) if oracle else 0.5          # oracle knows b; else uniform prior
    observed = oracle
    X = X0.clone()
    step = 0
    min_cue_dist = float("inf")
    while step < T_max:
        plan = efe_pomdp_plan(
            model, X, p=p, zgp=zgp, zgm=zgm, cgp=cgp, cgm=cgm, zc=zc, delta=delta,
            beta=(0.0 if observed else beta), R_noise=R_noise, w_t=w_t, gen=gen, **cem,
        )
        for kk in range(min(replan_every, T_max - step)):
            X = teacher_step(X, plan[kk : kk + 1])
            step += 1
            d_cue = float((model.encoder(X) - zc).norm())
            min_cue_dist = min(min_cue_dist, d_cue)
            if (not observed) and d_cue < delta:             # sense the cue -> collapse the belief
                p = 1.0 if b == 1 else 0.0
                observed = True
    R_resid = kabsch_rotation(X[0], Xtrue[0])
    return {
        "ang": rotation_angle_deg(R_resid),
        "pos": float((centroid(X) - centroid(Xtrue)).norm()),
        "sensed": bool(min_cue_dist < delta) if not oracle else False,
        "min_cue_dist": min_cue_dist,
        "delta": delta,
        "d": float((centroid(Xgp) - centroid(Xgm)).norm()) / 2.0,   # hedge-floor distance
    }


# --------------------------------------------------------------------------- #
# [C] SE(3) invariance of the salience field (the theorem, at the planner level)
# --------------------------------------------------------------------------- #
@torch.no_grad()
def _salience_eta(
    model, X0: torch.Tensor, Xc: torch.Tensor, cand: torch.Tensor, delta: float,
) -> torch.Tensor:
    r"""Imagined cue-sensing probability $\eta$ for a *fixed* candidate batch. ``cand:(ns,H,3)->(ns,)``.

    Input-level (re-encodes the clouds), so the invariance test below assumes nothing about how the
    latent transforms -- it just rotates clouds+actions and checks $\eta$ is unchanged.
    """
    ns = cand.shape[0]
    zc = model.encoder(Xc)
    z = model.encoder(X0).expand(ns, -1).contiguous()
    notsensed = torch.ones(ns, dtype=z.dtype)
    two_d2 = 2.0 * delta * delta
    for h in range(cand.shape[1]):
        z = model.predictor(z, cand[:, h])
        notsensed = notsensed * (1.0 - torch.exp(-((z - zc) ** 2).sum(-1) / two_d2))
    return 1.0 - notsensed


@torch.no_grad()
def salience_invariance(
    model, X0: torch.Tensor, Xc: torch.Tensor, R: torch.Tensor, t: torch.Tensor, *,
    H: int = 12, n_samples: int = 64, gen: torch.Generator | None = None,
) -> float:
    r"""$\max_n|\eta_n(X_0,X_c)-\eta_n(RX_0{+}t,\,RX_c{+}t,\,R\,\text{cand})|$ -- $0$ for the VN agent.

    A global $(R,t)$ rotates both clouds and the candidate actions; the salience depends only on the
    latent distance to the cue, so for the equivariant encoder it is unchanged to the float floor. The
    MLP control conflates pose with cue-proximity and breaks it.

    Caveat on the MLP side: $\eta=1-\prod_h(1-s_h)$ saturates ($\to0$ or, over many steps, $\to1$) when
    the sensor width $\delta$ is mismatched to the latent scale, which can make a *collapsed /
    lightly-trained* non-equivariant latent look vacuously invariant *in this scalar*. The VN invariance
    is exact for any $\delta$; the well-trained experiment latent (60 epochs) puts $\eta$ in its
    sensitive band and the MLP break is then large ($0.9$). The robust, training-independent MLP break
    is the **EFE-plan** equivariance (:func:`efe_pomdp_plan`), which the unit test guards.
    """
    z0, zc = model.encoder(X0), model.encoder(Xc)
    delta = 0.6 * float((zc - z0).norm()) + 1e-9
    cand = _ball_clamp(0.6 * torch.randn(n_samples, H, 3, generator=gen, dtype=X0.dtype))
    eta = _salience_eta(model, X0, Xc, cand, delta)
    cand_r = torch.einsum("ij,nhj->nhi", R.to(X0.dtype), cand)         # rotate the candidate actions
    eta_r = _salience_eta(model, _apply(X0, R, t), _apply(Xc, R, t), cand_r, delta)
    return (eta - eta_r).abs().max().item()


